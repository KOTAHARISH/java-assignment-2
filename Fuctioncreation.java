
public class Fuctioncreation {

	static void additionofTwo(){
		int x = 10;
		int y = 30;
		int a = x+y ;
		System.out.println(a);
	}
	static void additionofThree(){
		int x = 10;
		int y = 30;
		int z = 40;
		int a = x+y+z ;
		System.out.println(a);
	}
	static void multicationofTwo(){
		int x = 10;
		int y = 30;
		int m = x*y ;
		System.out.println(m);
	}
	static void multicationofThree(){
		int x = 10;
		int y = 30;
		int z = 40;
		int m = x*y*z ;
		System.out.println(m);
	}
	public static void main(String[] args) {
		
		//we have call the methods
		additionofTwo();
		additionofThree();
		multicationofTwo();
		multicationofThree();

	}

}
